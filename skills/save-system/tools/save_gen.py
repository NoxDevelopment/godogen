"""Save System — Godot 4 save-system scaffold generator.

Emits four pieces, individually or together:
  save_data.gd       Typed Resource model with versioned fields + migrate_from().
  save_manager.gd    Autoload singleton: slots, atomic writes, dirty flag.
  autosave.gd        Timer-driven autosave (skips writes when not dirty).
  thumbnail.gd       Viewport-capture helper for slot previews.

Subcommands
-----------
model      Emit save_data.gd from a named preset.
manager    Emit save_manager.gd.
autosave   Emit autosave.gd.
thumbnail  Emit thumbnail.gd.
all        Emit all four (one-shot project scaffold).
list       List available presets and their field sets.

Pure text — no ComfyUI / Tripo3D / etc.
"""

