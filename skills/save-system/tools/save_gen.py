MANAGER_GD = '''## Auto-generated by save-system skill. Register as Autoload `SaveManager`.
## Atomic writes via .tmp + rename. Multi-slot. Cached in-memory.
extends Node

const SAVE_DIR := "user://saves/"
const SLOT_PREFIX := "slot_"
const SAVE_EXT := ".tres"
const TMP_EXT := ".tmp"

signal save_completed(slot: int, success: bool)
signal load_completed(slot: int, success: bool, data: Resource)

var _dirty: bool = false
var _cache: Dictionary = {}  # slot:int -> SaveData


func _ready() -> void:
    DirAccess.make_dir_recursive_absolute(SAVE_DIR)


# --- Dirty flag ------------------------------------------------------------

func mark_dirty() -> void:
    _dirty = true

func is_dirty() -> bool:
    return _dirty

func clear_dirty() -> void:
    _dirty = false


# --- Paths ----------------------------------------------------------------

func slot_path(slot: int) -> String:
    return "%s%s%d%s" % [SAVE_DIR, SLOT_PREFIX, slot, SAVE_EXT]

func slot_thumb_path(slot: int) -> String:
    return "%s%s%d.png" % [SAVE_DIR, SLOT_PREFIX, slot]


# --- Slot enumeration -----------------------------------------------------

func list_slots() -> Array:
    var rows: Array = []
    var dir := DirAccess.open(SAVE_DIR)
    if dir == null:
        return rows
    dir.list_dir_begin()
    var name := dir.get_next()
    var slots_seen: Dictionary = {}
    while name != "":
        if name.begins_with(SLOT_PREFIX) and name.ends_with(SAVE_EXT):
            var slot_str := name.substr(SLOT_PREFIX.length(), name.length() - SLOT_PREFIX.length() - SAVE_EXT.length())
            if slot_str.is_valid_int():
                slots_seen[int(slot_str)] = true
        name = dir.get_next()
    dir.list_dir_end()
    for slot in slots_seen.keys():
        var path := slot_path(slot)
        var modified := FileAccess.get_modified_time(path)
        var summary := ""
        var data: SaveData = _cache.get(slot)
        if data == null:
            data = _peek_summary(slot)
        if data != null:
            summary = data.summary()
        rows.append({
            "slot": slot,
            "exists": true,
            "modified_time": modified,
            "thumbnail_path": slot_thumb_path(slot),
            "summary": summary,
        })
    rows.sort_custom(func(a, b): return a["slot"] < b["slot"])
    return rows


func _peek_summary(slot: int) -> SaveData:
    # Lightweight load just to call .summary(); does NOT populate the cache,
    # since the caller didn't ask for the full object.
    var path := slot_path(slot)
    if not FileAccess.file_exists(path):
        return null
    var res := ResourceLoader.load(path, "Resource", ResourceLoader.CACHE_MODE_IGNORE)
    if res is SaveData:
        return res
    return null


# --- Save -----------------------------------------------------------------

func save_to_slot(slot: int, data: SaveData) -> Error:
    if data == null:
        push_error("SaveManager.save_to_slot(%d): data is null" % slot)
        save_completed.emit(slot, false)
        return ERR_INVALID_PARAMETER
    DirAccess.make_dir_recursive_absolute(SAVE_DIR)
    var final_path := slot_path(slot)
    var tmp_path := final_path + TMP_EXT
    data.version = SaveData.VERSION
    var err := ResourceSaver.save(data, tmp_path)
    if err != OK:
        push_error("SaveManager.save_to_slot(%d): write failed (%d)" % [slot, err])
        save_completed.emit(slot, false)
        return err
    # Atomic rename. DirAccess.rename overwrites the destination on Windows + Unix.
    err = DirAccess.rename_absolute(ProjectSettings.globalize_path(tmp_path),
                                    ProjectSettings.globalize_path(final_path))
    if err != OK:
        # Some filesystems require explicit remove-before-rename.
        DirAccess.remove_absolute(ProjectSettings.globalize_path(final_path))
        err = DirAccess.rename_absolute(ProjectSettings.globalize_path(tmp_path),
                                        ProjectSettings.globalize_path(final_path))
    if err != OK:
        push_error("SaveManager.save_to_slot(%d): rename failed (%d)" % [slot, err])
        save_completed.emit(slot, false)
        return err
    _cache[slot] = data
    clear_dirty()
    save_completed.emit(slot, true)
    return OK


# --- Load -----------------------------------------------------------------

func load_from_slot(slot: int) -> SaveData:
    var path := slot_path(slot)
    if not FileAccess.file_exists(path):
        load_completed.emit(slot, false, null)
        return null
    var res := ResourceLoader.load(path, "Resource", ResourceLoader.CACHE_MODE_IGNORE)
    var data: SaveData = null
    if res is SaveData:
        data = res
        # Run migration if the loaded resource's version is below current.
        if data.version != SaveData.VERSION:
            var as_dict := _resource_to_dict(data)
            data = SaveData.migrate_from(as_dict)
    elif res is Resource:
        # Resource type changed (e.g., class renamed) — round-trip through Dictionary.
        push_warning("SaveManager.load_from_slot(%d): loaded resource is not a SaveData (%s); attempting migration." % [slot, res.get_class()])
        var as_dict := _resource_to_dict(res)
        data = SaveData.migrate_from(as_dict)
    if data == null:
        load_completed.emit(slot, false, null)
        return null
    _cache[slot] = data
    load_completed.emit(slot, true, data)
    return data


func _resource_to_dict(res: Resource) -> Dictionary:
    var d: Dictionary = {}
    for prop in res.get_property_list():
        var name: String = prop.get("name", "")
        # Skip engine-internal properties.
        if name.is_empty() or name.begins_with("_") or name in ["resource_local_to_scene",
                "resource_name", "resource_path", "script"]:
            continue
        d[name] = res.get(name)
    return d


# --- Delete ---------------------------------------------------------------

func delete_slot(slot: int) -> Error:
    var path := slot_path(slot)
    var thumb := slot_thumb_path(slot)
    _cache.erase(slot)
    if FileAccess.file_exists(thumb):
        DirAccess.remove_absolute(ProjectSettings.globalize_path(thumb))
    if FileAccess.file_exists(path):
        return DirAccess.remove_absolute(ProjectSettings.globalize_path(path))
    return ERR_FILE_NOT_FOUND


# --- Quick save shortcut --------------------------------------------------

func quick_save(data: SaveData) -> Error:
    return save_to_slot(0, data)

func quick_load() -> SaveData:
    return load_from_slot(0)
'''


