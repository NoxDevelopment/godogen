# Writers
# ---------------------------------------------------------------------------

def _resolve_output_dir(arg: str) -> Path:
    """Accept both filesystem and res:// paths. Strip res:// to a relative
    path; the agent is expected to be running from the project root."""
    if arg.startswith("res://"):
        return Path(arg[len("res://"):])
    return Path(arg)


def cmd_model(args) -> None:
    out_dir = _resolve_output_dir(args.output)
    out_dir.mkdir(parents=True, exist_ok=True)
    path = out_dir / "save_data.gd"
    path.write_text(_emit_save_data(args.preset), encoding="utf-8")
    print(json.dumps({"ok": True, "wrote": str(path), "preset": args.preset,
                      "fields": len(PRESETS[args.preset]) + 1}, indent=2))


def cmd_manager(args) -> None:
    out_dir = _resolve_output_dir(args.output)
    out_dir.mkdir(parents=True, exist_ok=True)
    path = out_dir / "save_manager.gd"
    path.write_text(MANAGER_GD, encoding="utf-8")
    print(json.dumps({
        "ok": True, "wrote": str(path),
        "next_step": "Register as Autoload named 'SaveManager' "
                     "(Project Settings -> Autoload). Must run before any "
                     "scene that calls save/load.",
    }, indent=2))


def cmd_autosave(args) -> None:
    if args.interval <= 0:
        raise SystemExit("--interval must be > 0")
    out_dir = _resolve_output_dir(args.output)
    out_dir.mkdir(parents=True, exist_ok=True)
    path = out_dir / "autosave.gd"
    path.write_text(AUTOSAVE_GD.format(slot=args.slot, interval=args.interval),
                    encoding="utf-8")
    print(json.dumps({
        "ok": True, "wrote": str(path), "slot": args.slot, "interval_seconds": args.interval,
        "next_step": "Fill in _snapshot_state() with your project's gameplay state.",
    }, indent=2))


def cmd_thumbnail(args) -> None:
    out_dir = _resolve_output_dir(args.output)
    out_dir.mkdir(parents=True, exist_ok=True)
    if "x" not in args.size:
        raise SystemExit("--size must be WxH, e.g. 320x180")
    w_str, h_str = args.size.split("x", 1)
    w, h = int(w_str), int(h_str)
    path = out_dir / "thumbnail.gd"
    path.write_text(THUMBNAIL_GD.format(w=w, h=h), encoding="utf-8")
    print(json.dumps({"ok": True, "wrote": str(path), "size": [w, h]}, indent=2))


def cmd_all(args) -> None:
    cmd_model(args)
    cmd_manager(args)
    # Defaults for the bundled scaffold:
    class _A: pass
    a = _A(); a.output = args.output; a.slot = args.slot; a.interval = args.interval
    cmd_autosave(a)
    a2 = _A(); a2.output = args.output; a2.size = args.size
    cmd_thumbnail(a2)


def cmd_list(_args) -> None:
    rows = ["Available presets:"]
    for preset, fields in PRESETS.items():
        rows.append(f"  {preset:10s} ({len(fields)} fields)")
        for name, gd_type, default, _c in fields:
            rows.append(f"      {name:22s} {gd_type:18s} default={default}")
        rows.append("")
    print("\n".join(rows))


def main():
    parser = argparse.ArgumentParser(
        description="save-system: Godot 4 save-system scaffold generator")
    sub = parser.add_subparsers(required=True, dest="cmd")

    def _add_output(p):
        p.add_argument("--output", default="res://save/",
                       help="Output directory (default res://save/). Filesystem paths also accepted.")

    p = sub.add_parser("model", help="Emit save_data.gd")
    _add_output(p)
    p.add_argument("--preset", required=True, choices=list(PRESETS))
    p.set_defaults(func=cmd_model)

    p = sub.add_parser("manager", help="Emit save_manager.gd")
    _add_output(p)
    p.set_defaults(func=cmd_manager)

    p = sub.add_parser("autosave", help="Emit autosave.gd")
    _add_output(p)
    p.add_argument("--interval", type=float, default=120.0, help="Seconds between checks (default 120)")
    p.add_argument("--slot", type=int, default=9, help="Slot index for autosave (default 9)")
    p.set_defaults(func=cmd_autosave)

    p = sub.add_parser("thumbnail", help="Emit thumbnail.gd")
    _add_output(p)
    p.add_argument("--size", default="320x180", help="WxH (default 320x180)")
    p.set_defaults(func=cmd_thumbnail)

    p = sub.add_parser("all", help="Emit all four scripts")
    _add_output(p)
    p.add_argument("--preset", required=True, choices=list(PRESETS))
    p.add_argument("--interval", type=float, default=120.0)
    p.add_argument("--slot", type=int, default=9)
    p.add_argument("--size", default="320x180")
    p.set_defaults(func=cmd_all)

    p = sub.add_parser("list", help="List available presets")
    p.set_defaults(func=cmd_list)

    args = parser.parse_args()
    args.func(args)


if __name__ == "__main__":
    main()
