THUMBNAIL_GD = '''## Auto-generated by save-system skill. Capture a viewport screenshot,
## downsize, and write to the slot's thumbnail path. Call AFTER
## SaveManager.save_to_slot() (so the .tres exists when the slot picker
## scans the saves dir).
extends Node

const THUMB_WIDTH := {w}
const THUMB_HEIGHT := {h}


static func capture(slot: int) -> Error:
    var viewport := Engine.get_main_loop().root if Engine.get_main_loop() else null
    if viewport == null:
        return ERR_UNAVAILABLE
    var img := viewport.get_texture().get_image()
    if img == null:
        return ERR_UNAVAILABLE
    img.resize(THUMB_WIDTH, THUMB_HEIGHT, Image.INTERPOLATE_LANCZOS)
    var path := SaveManager.slot_thumb_path(slot)
    return img.save_png(path)
'''


