# ---------------------------------------------------------------------------
# save_data.gd emission
# ---------------------------------------------------------------------------

def _emit_save_data(preset: str) -> str:
    if preset not in PRESETS:
        raise SystemExit(f"Unknown preset {preset!r}. Available: {', '.join(PRESETS)}")
    fields = PRESETS[preset]

    lines: list[str] = []
    lines.append("## Auto-generated by save-system skill (preset: {0}).".format(preset))
    lines.append("## Bump VERSION and add a `migrate_from` branch when you change fields.")
    lines.append("class_name SaveData")
    lines.append("extends Resource")
    lines.append("")
    lines.append("const VERSION := 1")
    lines.append("")
    lines.append("@export var version: int = VERSION")
    for name, gd_type, default, comment in fields:
        lines.append(f"## {comment}")
        lines.append(f"@export var {name}: {gd_type} = {default}")
    lines.append("")
    lines.append("")
    lines.append("## Returns a one-line summary used by the slot picker UI.")
    lines.append("func summary() -> String:")
    if preset == "platformer":
        lines.append("    return \"%s · %d coins · %d lives\" % [level_id, coins, lives]")
    elif preset == "topdown":
        lines.append("    return \"%s · HP %d\" % [scene_path.get_file().get_basename(), player_health]")
    elif preset == "rpg":
        lines.append("    return \"%s · %d gold · party %d\" % [scene_path.get_file().get_basename(), gold, party_members.size()]")
    elif preset == "puzzle":
        lines.append("    return \"%d solved · %d hints\" % [solved_puzzles.size(), hint_tokens]")
    elif preset == "racing":
        lines.append("    return \"%d tracks · $%d\" % [unlocked_tracks.size(), cash]")
    else:
        lines.append("    return \"v%d\" % version")
    lines.append("")
    lines.append("")
    lines.append("## Walks an old Dictionary representation up to the current schema.")
    lines.append("## When you bump VERSION, ADD a branch here; never delete an old one.")
    lines.append("static func migrate_from(old: Dictionary) -> SaveData:")
    lines.append("    var data := SaveData.new()")
    lines.append("    var old_version: int = int(old.get(\"version\", 1))")
    lines.append("    # Example migration scaffold — replace with real branches as you bump VERSION:")
    lines.append("    # if old_version == 1:")
    lines.append("    #     old[\"new_field\"] = default_value")
    lines.append("    #     old_version = 2")
    lines.append("    if old_version != VERSION:")
    lines.append("        push_warning(\"SaveData.migrate_from: no migration branch for v%d -> v%d. Defaults will be used for missing fields.\" % [old_version, VERSION])")
    lines.append("    for key in old:")
    lines.append("        if data.get(key) != null or key in data:")
    lines.append("            data.set(key, old[key])")
    lines.append("    data.version = VERSION")
    lines.append("    return data")
    lines.append("")
    return "\n".join(lines)


